# Define a string nome
nome = "Marcus_Kuhn"

# Itera sobre os caracteres da string nome usando a função enumerate
# A função enumerate retorna um índice (i) e o valor (c) correspondente da lista ou string
for i, c in enumerate(nome):
    # Imprime a posição (índice) e o valor (caractere) correspondente
    print(f"Posição = {i}, valor = {c}")
